interface FileNode {
  name: string;
  type: 'file' | 'folder';
  children?: FileNode[];
  content?: string;
  path?: string;
}

function FileTreeItem({
  node,
  depth,
  selectedFile,
  onSelect,
}: {
  node: FileNode;
  depth: number;
  selectedFile: string;
  onSelect: (node: FileNode) => void;
}) {
  const [expanded, setExpanded] = useState(depth === 0);
  const isSelected = selectedFile === node.name;

  const getFileIcon = (name: string) => {
    if (name.endsWith('.py')) return <FileCode size={13} className="text-[#3572A5]" />;
    if (name.endsWith('.json') || name.endsWith('.yaml')) return <FileCode size={13} className="text-[#f59e0b]" />;
    if (name.endsWith('.md') || name.endsWith('.txt')) return <FileText size={13} className="text-[#6366f1]" />;
    if (name.endsWith('.tex') || name.endsWith('.bib')) return <FileText size={13} className="text-[#10a37f]" />;
    return <FileCode size={13} className="text-[#999]" />;
  };

  return (
    <div>
      <button
        onClick={() => {
          if (node.type === 'folder') setExpanded(!expanded);
          else onSelect(node);
        }}
        className={`w-full flex items-center gap-1.5 px-2 py-1.5 text-xs rounded-lg transition-all ${
          isSelected
            ? 'bg-[#10a37f]/10 text-[#10a37f] font-medium'
            : 'text-[#666] dark:text-[#aaa] hover:bg-[#f4f4f4] dark:hover:bg-[#2a2a3e]'
        }`}
        style={{ paddingLeft: `${depth * 14 + 8}px` }}
      >
        {node.type === 'folder' ? (
          <>
            {expanded ? <ChevronDown size={12} className="text-[#999] shrink-0" /> : <ChevronRight size={12} className="text-[#999] shrink-0" />}
            <FolderOpen size={13} className="text-[#f59e0b] shrink-0" />
          </>
        ) : (
          <>
            <span className="w-3 shrink-0" />
            {getFileIcon(node.name)}
          </>
        )}
        <span className="truncate">{node.name}</span>
      </button>
      {node.type === 'folder' && expanded && node.children && (
        <div>
          {node.children.map((child) => (
            <FileTreeItem
              key={child.name}
              node={child}
              depth={depth + 1}
              selectedFile={selectedFile}
              onSelect={onSelect}
            />
          ))}
        </div>
      )}
    </div>
  );
}

